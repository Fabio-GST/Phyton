# Elaborar um programa que calcule e apresente o volume de uma caixa retangular, por meio da fórmula
# VOLUME ← COMPRIMENTO * LARGURA * ALTURA

Comprimento = float(input("\nDigite o valor do Comprimento: "))
Largura = float(input("\nDigite o valor da Largura: "))
Altura = float(input("\nDigite o valor da Altura: "))
Volume = Comprimento * Largura * Altura
print("\nO volume da Caixa é: ", Volume)  